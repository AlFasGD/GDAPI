namespace GDAPI.Enumerations
{
    /// <summary>Represents a note value.</summary>
    public enum MusicalNoteValue
    {
        /// <summary>Represents a whole note.</summary>
        Whole = 1,
        /// <summary>Represents a half note.</summary>
        Half = 2,
        /// <summary>Represents a quarter note.</summary>
        Quarter = 4,
        /// <summary>Represents an eighth note.</summary>
        Eighth = 8,
        /// <summary>Represents a sixteenth note.</summary>
        Sixteenth = 16,
        /// <summary>Represents a thirty-second note.</summary>
        ThirtySecond = 32,
        /// <summary>Represents a sixty-fourth note.</summary>
        SixtyFourth = 64,
        /// <summary>Represents a hundred-twenty-eighth note.</summary>
        HundredTwentyEighth = 128,
    }
}
